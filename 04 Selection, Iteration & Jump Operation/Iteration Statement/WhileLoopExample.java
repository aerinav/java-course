public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("Iterasi: " + i);
            i++;
        }

        int j = 10;
        while (j >= 0) {
            System.out.println("Iterasi: " + j);
            j--;
        }
    }
}
