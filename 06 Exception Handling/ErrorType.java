public class ErrorType {
    public static void main(String[] args) {
        // Jenis-jenis Error
        // 1. Syntax Errors
            // Kesalahan ini terjadi saat penulisan kode tidak sesuai dengan aturan sintaksis bahasa Java.
            // Biasanya terdeteksi oleh compiler sebelum program dijalankan.
            // Contoh: lupa menambahkan titik Object pernyataan koma (;) di akhir pernyataan, penggunaan tanda kurung yang tidak tepat, dsb.
        // 2. Logical Errors
            // Kesalahan ini terjadi ketika program berjalan tanpa masalah, tetapi hasil yang diperoleh tidak sesuai dengan yang diharapkan.
            // Biasanya disebabkan oleh kesalahan dalam logika atau algoritma program.
            // Tidak terdeteksi oleh compiler atau runtime environment.
        // 3. Runtime Errors
            // Kesalahan ini terjadi saat program dijalankan dan menyebabkan program berhenti secara tiba-tiba.
            // Biasanya disebabkan oleh kondisi yang tidak terduga, seperti pembagian dengan nol, akses indeks array yang di luar batas, atau null pointer exception.
    }
}