public class VariabelDanTipeData3 {
    public static void main(String[] args) {
        // Ada dua cara penulisan variabel
        // Cara 1. Deklarasi & Inisialisasi
        int angka1 = 5;
        int angka2 = 76;
        int angka3 = 80;
        int angka4 = 100;
        int angka5 = 500;

        System.out.println(angka1);
        System.out.println(angka2);
        System.out.println(angka3);
        System.out.println(angka4);
        System.out.println(angka5);

        // Cara 2. Deklarasi dulu
        int angka6, angka7, angka8, angka9, angka10;

        // Baru Inisialisasi (Memberi nilai variabel)
        angka6 = 150;
        angka7 = 200;
        angka8 = 250;
        angka9 = 300;
        angka10 = 400;

        System.out.println(angka6);
        System.out.println(angka7);
        System.out.println(angka8);
        System.out.println(angka9);
        System.out.println(angka10);

        // Re-inisialisasi (Mengubah nilai variabel)
        angka6 = 600;
        angka7 = 700;
        angka8 = 800;
        angka9 = 900;
        angka10 = 1000;

        // Hasil setelah Re-inisialisasi
        System.out.println(angka6);
        System.out.println(angka7);
        System.out.println(angka8);
        System.out.println(angka9);
        System.out.println(angka10);
    }
}
